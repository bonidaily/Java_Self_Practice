package day_03_operators.operators_practice_tasks;

public class GallonsToLiters {

    public static void main(String[] args) {

        double gallon = 14;
        double liters = 3.79;

        double gallonsToLiters = (gallon * liters);

        System.out.println(gallon + " gallons " + "is equal to " + gallonsToLiters + " liters.");


    }

}

